def makedocs(numdocs)

  @docs = Array.new

  for i in 1..numdocs

    qr = "USWIQUOTE"+(250000000000+rand(100000000)).to_s
    quoterequest =
        {
            :time => Time.now,
            :quoteReference => qr,
            :originalXML => @xml
        }

    @docs << quoterequest

  end
end



